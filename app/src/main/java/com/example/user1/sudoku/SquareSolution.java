package com.example.user1.sudoku;

public class SquareSolution {

    public int row, column, value;

    public SquareSolution() {
        row = 0;
        column = 0;
        value = 0;
    }

    public SquareSolution(int row, int column, int value) {
        this.row = row;
        this.column = column;
        this.value = value;
    }
}
